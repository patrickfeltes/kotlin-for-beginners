/**
 * This is a multiline comment
 * These comments are not read when we run our code
 * They're just for us programmers to make sense of what we are doing!
 */

// this is the main function, the entry-point of a program
// it's where everything starts from
fun main(args: Array<String>) {

    // this is a print statement and gives us the ability to output stuff to the user
    // print statements stay on the same line
    print("Hello")
    print(", World!\n") // \n is a newline character and makes a new line!

    // println statements are the same way, but they add a newline to the end
    println("Hello, World!")
    println("My name is Patrick!")

    // we can even print different things, like numbers and booleans!
    println(1)
    println(true)
    println(1/2)
}
